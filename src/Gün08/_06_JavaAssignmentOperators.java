package Gün08;

public class _06_JavaAssignmentOperators {
    public static void main(String[] args) {

     int a=10; // a ya 10 değeri atandı

     a=5; //a ya 5 değeri atanmış olur

     a=a+6; //a ya 6 değerini ekle11 oldu
     a+=6;  //a ya 6 değerini ekle 17 olur
     //a nın artış değeri 6 ya eşit

     a=a+1; //a yı bir arttır
     a++;  //a yı 1 arttır
     ++a; // a yı 1 arttır
     a+=1; // a yı 1 arttır

    //****************
     a=a-1; // a yı 1 azalt
     a--;   //a yı 1 azalt
     --a;   // a yı 1 azalt
     a-=1;  // a yı 1 azaltır

    //********************
    int b=10;
    b=b/5;  // b yi 5e böl
    b/=5;   //b yi 5 e böl

    //***************
    int c=10;
    c=c*5; // c yi 5 ile çarp 50
    c*=5;  // c yi 5 ile çarp 50





























    }
}

package Gün14;

public class _05_ForLoop {
    public static void main(String[] args) {

      //0 dan 10 (dahil) kadar sayıları ekrana yazdırınnız.
      //10 dan 0 (dahil) kadar sayıları ekrana yazdırınız.(ayrı bir diğer for)



      for ( int i=0; i<=10; i++) { //sayaç i,  sayaç2  j
          System.out.println(i);
      }

        for (int i = 10; i>0 ; i--) {
            System.out.println(i);

        }



















    }
}

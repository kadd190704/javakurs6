package gün02;

public class _05_Örnek {
    public static void main(String[] args) {
        //   \n = yeni satır aç , alt satıra geç
        //   \t  = tab tusuna bas, 2  ,3 harflik boşluk oluşturur
        //   \b  = backspace etkisi oluşturur, yani bir karakter geri siler
        //   \"  = tırnak yazdırmak için kullanılır
        //   \\  = ekrana bir tane \ yazar
        //    \r = satır başına git


        System.out.println("Merhabadünya");
        System.out.println("Merhaba\nDünya");
        System.out.println("merhaba\tDünya");
        System.out.println("Merhaba\bDünya");
        System.out.println("Merhaba\"Dünya");
        System.out.println("Merhaba\\ Dünya");
        System.out.println("Merhaba\rDünya");





    }
}
